--H2 DB
CREATE TABLE BOARD
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY, -- 값이 NULL이면 자동으로 값을 1증가하여 채워줌
    title VARCHAR(255) NULL,
    content VARCHAR_IGNORECASE NULL,
    writer VARCHAR(50) NULL,
    regdate DATE NULL,
    hit INT NULL,
PRIMARY KEY (id)
);
COMMENT ON TABLE BOARD IS '게시판';
COMMENT ON COLUMN BOARD.id IS '게시글 번호';
COMMENT ON COLUMN BOARD.title IS '제목';
COMMENT ON COLUMN BOARD.content IS '본문';
COMMENT ON COLUMN BOARD.writer IS '작성자';
COMMENT ON COLUMN BOARD.regdate IS '작성일';
COMMENT ON COLUMN BOARD.hit IS '조회수';